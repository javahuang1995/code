package com.model;
public class User {
	//name属性
    private String name;
    //age属性
    private int age;
    //tel属性
    private String tel;
    //各属性的getter和setter()方法
    public String getName() {
    	return name;
    }
    public void setName(String name) {
    	this.name = name;
    }
    public int getAge() {
    	return age;
    }
    public void setAge(int age) {
    	this.age = age;
    }
    public String getTel() {
    	return tel;
    }
    public void setTel(String tel) {
    	this.tel = tel;
    }
}
