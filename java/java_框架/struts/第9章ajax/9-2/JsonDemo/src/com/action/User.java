package com.action;

public class User {
//name属性
private String name;
//age属性
private int age;
//getter方法
public String getName() {
	return name;
}
//setter方法
public void setName(String name) {
	this.name = name;
}
//age属性getter方法
public int getAge() {
	return age;
}
//age属性setter方法
public void setAge(int age) {
	this.age = age;
}
}
